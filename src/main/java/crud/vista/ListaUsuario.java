    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (txtNombre.getText().equals("") || txtaPaterno.getText().equals("")
                || txtAmaterno.getText().equals("") || txtUsuario.getText().equals("")
                || txtClave.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "No puede dejar campos vacíos");
            return;
        }
        int usuario = new DAOUsuario().actualizar(Integer.parseInt(id), txtNombre.getText(), txtaPaterno.getText(),
                txtAmaterno.getText(), txtUsuario.getText(), txtClave.getText());
        if (usuario == 0) {
            JOptionPane.showMessageDialog(rootPane, "Sucedió un error al actualizar");
        }
        JOptionPane.showMessageDialog(rootPane, "Operación exitosa");
        txtNombre.setText("");
        txtaPaterno.setText("");
        txtAmaterno.setText("");
        txtUsuario.setText("");
        txtClave.setText("");
        obtenerDatos();
    }//GEN-LAST:event_btnModificarActionPerformed

