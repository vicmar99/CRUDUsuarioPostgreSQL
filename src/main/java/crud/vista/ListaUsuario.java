    public void obtenerDatos() {
        List<Usuario> usuarios = new DAOUsuario().obtenerRegistros();
        DefaultTableModel modelo = new DefaultTableModel();

        String[] columnas = {"Id", "Nombre", "Apellido paterno", "Apellido materno", "Usuario", "Clave"};
        modelo.setColumnIdentifiers(columnas);

        for (Usuario usuario : usuarios) {
            String[] renglon = {String.valueOf(usuario.getId()), usuario.getNombre(), usuario.getaPaterno(), usuario.getaMaterno(), usuario.getUsuario(), usuario.getClave()};
            modelo.addRow(renglon);
        }
        tablaUsuarios.setModel(modelo);
    }

    public void usuarioSeleccionado() {

        int fila = tablaUsuarios.getSelectedRow();

        if (fila >= 0) {
            id = tablaUsuarios.getValueAt(fila, 0).toString();
            txtNombre.setText(tablaUsuarios.getValueAt(fila, 1).toString());
            txtaPaterno.setText(tablaUsuarios.getValueAt(fila, 2).toString());
            txtAmaterno.setText(tablaUsuarios.getValueAt(fila, 3).toString());
            txtUsuario.setText(tablaUsuarios.getValueAt(fila, 4).toString());
            txtClave.setText(tablaUsuarios.getValueAt(fila, 5).toString());
        }

    }

